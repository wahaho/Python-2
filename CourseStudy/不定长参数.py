def test(a,b,*args,**kwargs):
    print(a)
    print(b)
    print(args)
    print(kwargs)

# test(11,22,33,44,55,66,mm=77,nn=88)

A = [1,2,3]
B = {"aa":100,"bb":200}

#当列表/元组在当做实参传递的时候,如果前面有一个*,表示对其进行解包[111,222] ----->111,222
#当字典中作为一个实参进行传递的时候,如果前面有两个*,对其解包{“aa”:100,"bb":200}  ----> aa=100,bb=200
test(11,22,*A,**B,pp=B)